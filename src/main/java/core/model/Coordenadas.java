package core.model;

public class Coordenadas {

    Double Xdegrees;
    Double Xminutes;
    Double Xseconds;

    Double Ydegrees;
    Double Yminutes;
    Double Yseconds;

    public Coordenadas(Double xdegrees, Double xminutes, Double xseconds, Double ydegrees, Double yminutes, Double yseconds) {
        Xdegrees = xdegrees;
        Xminutes = xminutes;
        Xseconds = xseconds;
        Ydegrees = ydegrees;
        Yminutes = yminutes;
        Yseconds = yseconds;
    }
}
